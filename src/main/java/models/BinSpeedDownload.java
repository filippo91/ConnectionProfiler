package models;

public class BinSpeedDownload {
	private int asnum;
	private int bin;
	private int nRecords;
	
	public int getAsnum() {
		return asnum;
	}
	public void setAsnum(int asnum) {
		this.asnum = asnum;
	}
	public int getBin() {
		return bin;
	}
	public void setBin(int bin) {
		this.bin = bin;
	}
	public int getnRecords() {
		return nRecords;
	}
	public void setnRecords(int nRecords) {
		this.nRecords = nRecords;
	}
	
	
}
